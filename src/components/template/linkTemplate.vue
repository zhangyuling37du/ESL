<template>
  <div class="com-paint" v-loading="loading" element-loading-text="拼命加载中">
    <div class="template-search-left lf">
      <div v-for="item in typeList">
        <h2>{{item.tagTypeName}}</h2>
        <div class="esl" v-for="(tag,index) in item.scrTypeList" :id="tag.id" @click="tagType(item,tag,index)">
          <div :class="{'active':ind === tag}"><span>{{tag.scrTypeName}}</span> </div>
        </div>
      </div>
    </div>
    <div class="template-search-right rg" v-if="templateShow">
      <div class="com-paint-head">
        <div>
          <el-input v-model="inputValue" placeholder="请输入模板名称" size="small" style="width:179px;"></el-input>
          <el-button @click="search" size="small">
            <i class="ESLfont icon-sousuo" style="color:#116f7d"></i>
            <span style="color:#116f7d">搜索</span>
          </el-button>
          <el-button size="small" @click="addTemplate()">
            <i class="ESLfont icon-iconfontzengjia"></i>
            <span>新增</span>
          </el-button>
        </div>
      </div>
      <div class="template-search-right-content">
        <!-- template-search-li -->
        <div v-for="items in searchList" class="template-search-li" v-loading="loading" element-loading-text="拼命加载中">
          <div>
            <div class="template-search-img2">
              <div class="template-search-img">
                <img :src="items.templetUrl" alt="" @dblclick="tepAmend(items)" />
                <div class="img-cont">
                  <img src="static/img/template-search-02.png" alt="" @click="tepAmend(items)" />
                  <img src="static/img/template-search-03.png" alt="" @click="tepdelte(items)" />
                </div>
              </div>
            </div>
            <div>
              <p>模板名称:{{items.templetName}}</p>
              <p>关联标签:{{items.tagNum}} 个</p>
            </div>
          </div>
        </div>
      </div>
    </div>
    <div class="block rg">
      <el-pagination @current-change="handleCurrentChange" :current-page.sync="currentPage" :page-size="pageSize" layout="total, prev, pager, next, jumper" :total="count">
      </el-pagination>
    </div>
  </div>
  </div>
</template>
<script>
import { queryTagType, queryTempletPicList, deleteTemplet, queryTempletInfo } from 'api/template'
import { mapGetters } from 'vuex'

export default {
  data() {
    return {
      typeName: '',
      typeList: [],
      inputValue: '',
      currentPage: 1,
      pageSize: 6,
      // pageNumber: 10,
      count: 0,
      tag: '',
      templateShow: false,
      searchList: [],
      templateImgUrl: '',
      scrType: {
        scrTypeId: '',
        tagTypeId: '',
        scrTypeName: '',
        tagtypeHeigth: '',
        tagtypeWidth: '',
      },
      tagTypeId: '',
      scrTypeId: '',
      ind: "",
      loading: true
    }
  },
  watch: {
    currentPage(val) {
      this.queryTempletPicList()
    }
  },
  mounted() {
    this.queryTagType()
  },
  computed: {
    ...mapGetters([
      "baseUrl"
    ])
  },
  methods: {
    handleCurrentChange(val) {
      this.currentPage = val;
    },
    addTemplate(val) {
      this.$router.push({ name: 'templateAdd' });
      let x = JSON.stringify(this.scrType);
      this.$store.commit("SET_TEMSCRTYPE", x);
    },
    search() {
      this.queryTempletPicList()
    },
    goBack(val) {
      this.templateShow = false;
    },
    tagType(item, tag, index) {
      this.ind = tag;
      this.scrType.tagTypeId = item.tagTypeId
      this.scrType.scrTypeId = tag.scrTypeId
      this.scrType.scrTypeName = tag.scrTypeName
      this.scrType.tagtypeHeigth = tag.tagtypeHeigth
      this.scrType.tagtypeWidth = tag.tagtypeWidth
      this.tagTypeId = item.tagTypeId
      this.scrTypeId = tag.scrTypeId
      this.templateShow = true;
      this.search()
    },
    queryTempletPicList() {
      queryTempletPicList(this.inputValue, this.currentPage, this.pageSize, this.tagTypeId, this.scrTypeId, ).then(res => {
        console.log(res)
        if (res.resultStatus) {
          this.loading = false
          this.searchList = res.data
          let a = res.data
          let i = 0;
          for (i in a) {
            a[i].templetUrl = `${this.baseUrl}` + '/templet/' + a[i].templetUrl
            // console.log(a[i].templetUrl.status== 404)
            if (a[i].templetUrl.status !== 200) {
              // a[i].templetUrl = ''
            }
          }
          this.count = res.count;
        }
      })
    },
    queryTagType() {
      queryTagType().then(res => {
        if (res.resultStatus) {
          this.loading = false
          this.typeList = res.data
          this.tag = this.typeList
        }
      })
    },
    //修改
    tepAmend(val) {
      let templetId = {
        templetId: val.templetId
      }
      queryTempletInfo(templetId).then(res => {
        this.$router.push({ name: 'templateAmend', query: { id: val.templetId } })
      }).catch(error => error)
    },
    tepdelte(val) {
      let templetId = {
        templetId: val.templetId
      }
      this.$confirm('此操作将删除该项, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => {
        deleteTemplet(templetId).then(res => {
          if (res.resultStatus) {
            this.$message({
              type: 'success',
              message: '删除成功!'
            });
            this.queryTempletPicList()
          }
        }).catch(error => error)
      }).catch(() => {
        this.$message({
          type: 'info',
          message: '已取消删除'
        });
      })
    },
  }
}

</script>
